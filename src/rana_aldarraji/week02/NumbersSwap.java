package rana_aldarraji.week02;

public class NumbersSwap {
    public static void main(String[] args) {

        int a = 10;
        int b = 5 ;

        System.out.println("a = " + a);
        System.out.println("b = " + b);

        System.out.println("---------------");

        a = a - b; // a = 5
        b = b + a; // b = 10

        System.out.println("a = " + a);
        System.out.println("b = " + b);

    }
}
/*
Swap two variable's values without using a third variable.
 */